package com.example.calculator;

public class Calculation {
    public double sum(double a, double b){
        double c=a+b;
        return c;
    }
    public double sub(double a, double b){
        double c=a-b;
        return c;
    }
    public double multiply(double a, double b){
        double c=a*b;
        return c;
    }
    public double div(double a, double b){
        double c=a/b;
        return c;

    }

}
